package ie.tudublin;

public class Note {

    Note() {

    }

    private char note;
    private int duration;

    public char getNote() {
        return note;
    }

    public void setNote(char note) {
        this.note = note;
    }

    public int getDuration() {
        return duration;
    }

    public void setDuration() {
        this.duration = duration;
    }

}
